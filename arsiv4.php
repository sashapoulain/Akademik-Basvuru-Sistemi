                                Yeditepe Üniversitesi Rektörlüğünden:
                                Üniversitemize 2547 sayılı Yükseköğretim Kanunu'nun 31.Maddesi ile 09 Kasım 2018 tarihli, 30590 sayılı Resmi Gazetede yayımlanan ve "Öğretim Üyesi Dışındaki Öğretim Elemanı Kadrolarına Yapılacak Atamalarda Uygulanacak Merkezi Sınav ile Giriş Sınavlarına İlişkin Usul ve Esaslar Hakkında Yönetmelik "in ilgili maddeleri uyarınca unvanı ve nitelikleri belirtilen kadrolara Öğretim Elemanları alınacaktır.


                                <br><br>
                                Genel şartlar:.
                                a) 657 sayılı Devlet Memurları Kanununun 48 inci maddesinde belirtilen şartları taşımak,
                                b) b) ALES'ten en az 70, Yükseköğretim Kurulu tarafından kabul edilen merkezi yabancı dil sınavından en az 50 puan veya eşdeğerliği kabul edilen bir sınavdan bu puan muadili bir puan almış olmak gerekir. Merkezi sınav muafiyetinden yararlanmayı talep edenlerin ön değerlendirme ve nihai değerlendirme aşamalarında ALES puanı 70 olarak kabul edilir.c) Ön değerlendirme ve nihai değerlendirme aşamalarında lisans mezuniyeti notunun hesaplanmasında kullanılacak 4'lük ve 5'lik not sistemlerinin 100'lük not sistemine eşdeğerliği Yükseköğretim Kurulu kararıyla belirlenir. Diğer not sistemlerinin 100'lük not sistemine eşdeğerliğine yükseköğretim kurumlarının senatoları karar verir.
                                d) Adaylarda, yabancı dille eğitim ve öğretim yapılan programlardaki öğretim görevlisi kadrolarına yapılacak atamalarda atama yapılacak programın eğitim dilinde; bilim alanı yabancı dille ilgili kadrolara yapılacak öğretim görevlisi atamaları ile 4/11/1981 tarihli ve 2547 sayılı Yükseköğretim Kanununun 5 inci maddesinin birinci fıkrasının (ı) bendi uyarınca zorunlu yabancı dil dersini vermek üzere öğretim görevlisi kadrolarına yapılacak atamalarda ilgili dilde; yükseköğretim kurulularının uluslararası ilişkiler ile yabancı dille ilgili uygulamalı birimlerinde istihdam edilecek öğretim görevlisi atamalarında, en az bir dilde Yükseköğretim Kurulu tarafından kabul edilen merkezi yabancı dil sınavından en az 80 puan veya eşdeğerliği kabul edilen bir sınavdan bu puan muadili bir puana sahip olma şartı aranır.
                                <br><br>
                                Özel Şartlar:
                                a) Araştırma görevlisi kadrosuna başvurabilmek için ilana ilk başvuru tarihi itibarıyla otuz beş yaşını doldurmamış olmak gerekir
                                b) Araştırma görevlisi kadrolarına başvurularda tezli yüksek lisans, doktora veya sanatta yeterlik eğitimi öğrencisi olmak şartı aranır.
                                c) Öğretim görevlisi kadrosuna başvuracak adaylarda en az tezli yüksek lisans derecesine sahip olmak şartı aranır.
